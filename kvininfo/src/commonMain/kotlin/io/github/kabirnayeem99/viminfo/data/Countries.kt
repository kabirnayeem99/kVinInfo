private val wmiCountryRangesByFirstChar: Map<Char, List<WmiCountryRange>> =
    wmiCountryRanges.groupBy { it.start[0] }

/**
 * Ranks a WMI second character per ISO 3780 ordering: letters A–Z (0–25), then digits 1–9 (26–34),
 * then 0 last (35).
 */
private fun secondCharRank(char: Char): Int =
    when (char) {
        in 'A'..'Z' -> char - 'A'
        in '1'..'9' -> 26 + (char - '1')
        '0' -> 35
        else -> -1
    }

private fun WmiCountryRange.contains(prefix: String): Boolean {
    if (prefix.isEmpty() || prefix[0] != start[0]) return false
    if (start.length == 1) return true // whole first-character block
    if (prefix.length < 2) return false
    val rank = secondCharRank(prefix[1])
    return rank in secondCharRank(start[1])..secondCharRank(end[1])
}

private fun countryFromRange(wmi: String): String? {
    if (wmi.isEmpty()) return null
    val prefix = wmi.take(2)
    return wmiCountryRangesByFirstChar[prefix[0]]?.firstOrNull { it.contains(prefix) }?.country
}

/**
 * Retrieves the country based on the World Manufacturer Identifier (WMI).
 *
 * Looks up the exact WMI in [wmiMap] first; if absent, falls back to the [wmiCountryRanges] table
 * keyed by the 2-character prefix.
 *
 * @param wmi The World Manufacturer Identifier.
 * @return The country name associated with the WMI.
 * @throws InvalidWmiForCountryException If the WMI matches neither the list nor any range.
 */
internal fun getCountryFromWmi(wmi: String): String {
    val country = wmiMap[wmi]
    if (country != null) return country
    return countryFromRange(wmi) ?: throw InvalidWmiForCountryException(wmi)
}
