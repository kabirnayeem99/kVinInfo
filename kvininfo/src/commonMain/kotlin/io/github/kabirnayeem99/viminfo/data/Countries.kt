/**
 * Retrieves the country based on the World Manufacturer Identifier (WMI).
 *
 * This function searches for the WMI in a predefined list and returns the corresponding country name.
 *
 * @param wmi The World Manufacturer Identifier.
 * @return The country name associated with the WMI.
 * @throws InvalidWmiForCountryException If the WMI is not found in the list.
 */
internal fun getCountryFromWmi(wmi: String): String {
    val index = wmiList.indexOf(wmi)
    if (index == -1) throw InvalidWmiForCountryException(wmi)
    return countryNames[index]
}